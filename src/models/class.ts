import mongoose from 'mongoose'

const classSchema = new mongoose.Schema({
  userId: {
    type: mongoose.Schema.Types.ObjectId,
    ref: 'User',
    required: true
  },
  name: {
    type: String,
    required: true,
    trim: true
  },
  description: {
    type: String,
    trim: true
  },
  studySets: [{
    type: mongoose.Schema.Types.ObjectId,
    ref: 'StudySet'
  }],
  createdAt: {
    type: Date,
    default: Date.now
  },
  updatedAt: {
    type: Date,
    default: Date.now
  }
})

export const Class = mongoose.models.Class || mongoose.model('Class', classSchema)
